####################################################################
#------------------------- Feature Function
function checkUser() {
  # $1为给定的用户名
  if [[ $USER == "$1" ]]; then
    echo -e "\n\\e[31m[✘]\\e[0m 当前用户是$1，拒绝执行！！！"
    exit $normal
  fi
}

